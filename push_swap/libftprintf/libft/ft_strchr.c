#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	if (c == 0)
		return ((char *)s + ft_strlen(s));
	while (*s != 0)
	{
		if (*s == c % 256)
		{
			return ((char *)s);
		}
		s++;
	}
	return (0);
}
